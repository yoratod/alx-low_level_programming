#include <stdio.h>
#include "dog.h"
/**
 * main - dog structure.
 *
 * @d: dog structure.
 * @name: the name of the name.
 * @age: the name of the age.
 * @owner: the name of the owner.
 * Return: Nothing.
 *
 **/
int main(struct dog *d, char *name, float age, char *owner)
{
	if (d != null)
	{
		d->name = name;
		d->age = age;
		d->owner = owner;
	}
};
